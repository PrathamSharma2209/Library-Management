    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
         String mid = txtid.getText();
        BookItem bitem= (BookItem)txtbook.getSelectedItem();
       SimpleDateFormat date_format = new SimpleDateFormat("yyyy-MM-dd");
        String issuedate = date_format.format(txtbdate.getDate());
       SimpleDateFormat date_format1 = new SimpleDateFormat("yyyy-MM-dd");
        String returndate = date_format1.format(txtrdate.getDate());
       
        
        try {
            pst = con.prepareStatement("insert into issuebook(mid,bid,issuedate,returndate)values(?,?,?,?)");
            pst.setString(1, mid);
            pst.setInt(2, bitem.id);
            pst.setString(3, issuedate);
            pst.setString(4, returndate);
          
           int k =  pst.executeUpdate();
           
           
           if (k==1){
               JOptionPane.showMessageDialog(this, "Book is Issued");
               txtid.setText("");
               txtbook.setSelectedIndex(-1);
               txtmember.setText("");
               txtid.requestFocus();
               IssueBook_Load();
        }
           else{
                JOptionPane.showMessageDialog(this, "Error");
           }
        } catch (SQLException ex) {
            Logger.getLogger(Issuebook.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

